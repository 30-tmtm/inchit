/** 해당 월령의 전체 인칫 포인트 항목 수 */
export function getKdstTotal(months: number): number {
  const range = getKdstRange(months);
  const key = `${range.start}-${range.end}` as KdstRangeKey;
  return KDST_ITEMS[key].reduce((sum, domain) => sum + domain.length, 0);
}
